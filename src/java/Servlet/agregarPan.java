            /* TODO output your page here. You may use following sample code. */
            String nombre;
            int stock, idcpan,idsabor;
            double precio;
            boolean registrar;
            
            nombre=request.getParameter("nombre");
            precio=Double.parseDouble(request.getParameter("precio"));
            stock=Integer.parseInt(request.getParameter("stock"));
            idcpan=Integer.parseInt(request.getParameter("tipo"));
            idsabor=Integer.parseInt(request.getParameter("sabor"));
            System.out.println("Pan:"+nombre+precio+stock+idcpan+idsabor);
            
